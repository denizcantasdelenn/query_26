--select * from subscriber


with cte as (
select *, 
case when sender < receiver then sender else receiver end as first, 
case when sender > receiver then sender else receiver end as second
from subscriber)

select sms_date, first, second, sum(sms_no)
from cte
group by sms_date, first, second